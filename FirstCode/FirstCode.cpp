// Include the iostream library which provides input/output functionality (cin, cout)
#include <iostream>

// Use the standard namespace to avoid writing std:: before cout, cin, etc.
using namespace std;

// Main function - the entry point of every C++ program
// int indicates this function returns an integer value
int main()
{
    // cout is used to print output to the console
    // << is the insertion operator that sends data to cout
    // "hello world" is a string literal that will be printed
    // "\n" creates a new line after the text
    cout <<"hello world" << "\n";
    // return 0 indicates the program executed successfully
    // 0 means no errors, non-zero values indicate errors
    return 0;
}

