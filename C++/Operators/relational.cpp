#include <iostream>
using namespace std;

int main()
{
    cout << (3<5) << endl; // true -> 1
    cout << (3>5) << endl; // false -> 0
    cout << (3<=3) << endl; // true-> 1
    cout << (3==3) << endl; // true -> 1
    cout << (1==0) << endl; // false -> 0
    cout << (1!=0) << endl; // true -> 1
    cout << (1!=1) << endl; // false -> 0

    return 0;
}

//Relational operators : "<,>,<=,>=,==,!="
// They will always retun TRUE or FALSE